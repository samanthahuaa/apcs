public class stringConcat {
  public static void main(String[] args) {
    String x = "h";
    String y = "e";
    String z = "l";
    String i = "l";
    String j = "o";
    System.out.println(x+y+z+i+j);
    System.out.println("1" + new String("2") + 3 + 4);
    System.out.println(1+2+3);
    System.out.println(1+2+3+"4");
  }
}
