public class varTypes {
  public static void main(String[] args) {
    int i = 89;
    System.out.println("int:"+i);

    byte j = 89;
    System.out.println("byte:" +j);

    short k = 89;
    System.out.println("short:"+k);

    double l = 89;
    System.out.println("double:"+l);

    long m = 89;
    System.out.println("long:"+m);

  }
}
